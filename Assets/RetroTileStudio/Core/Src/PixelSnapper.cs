using UnityEngine;
using System.Collections;

namespace RTStudio 
{
	public class PixelSnapper : MonoBehaviour {

		/// ====================================================================================================================================================================== 
		/// <summary>
		/// Raises the enable event.
		/// </summary>
		/// ====================================================================================================================================================================== 
		void OnEnable () {
			this.AddPixelPerfectObject();
		}
		
		/// ====================================================================================================================================================================== 
		/// <summary>
		/// Raises the disable event.
		/// </summary>
		/// ====================================================================================================================================================================== 
		void OnDisable () {
			this.RemovePixelPerfectObject();
		}
	}
}
